package com.spotts.json_parser;

import com.fasterxml.jackson.core.JsonProcessingException;
import com.fasterxml.jackson.databind.ObjectMapper;

import java.io.File;
import java.io.IOException;

public class RecordService {

    public static void parseJson() throws IOException {
        ObjectMapper objectMapper = new ObjectMapper();
        Record newRecord = objectMapper.readValue(new File("src/test/resources/single_record.json"), Record.class);
        System.out.println(newRecord.getTimestamp());
        System.out.println(newRecord.getCategory());
        System.out.println(newRecord.getValue());
    }

    public static void parseJson(String jsonString) throws JsonProcessingException {
        ObjectMapper objectMapper = new ObjectMapper();
        Record newRecord = objectMapper.readValue(jsonString, Record.class);
        System.out.println(newRecord.getTimestamp());
        System.out.println(newRecord.getCategory());
        System.out.println(newRecord.getValue());
    }
}
